package tema1.t01;

public class ej07 {
    public static void main(String[] args) {
        int X=5;
        int Y=8;

        double N=3.14;
        double M=6.7;

        System.out.println(X + "+" + M + "= " + (X+M));
        System.out.println(Y + "*" + N + "= " + (Y*N));
        System.out.println(N + "-" + Y + "= " + (N-Y));
        System.out.println(M + "/" + X + "= " + (M/X));
    }
}
