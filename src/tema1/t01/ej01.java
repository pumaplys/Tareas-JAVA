package tema1.t01;

class ej01
{
    public static void main (String[] args)
    {
        int datol, dato2, resultado;
        datol = 20;
        dato2 = 10;

// Suma
        resultado = datol + dato2;
        System.out.println(datol + " + " + dato2 + " = " + resultado);

// Resta
        resultado = datol - dato2;
        System.out.println(datol + " - " + dato2 + " = " + resultado);

// Producto
        resultado = datol * dato2;
        System.out.println(datol + " * " + dato2 + " = " + resultado);

// Cociente
        resultado = datol / dato2;
        System.out.println(datol + " / " + dato2 + " = " + resultado);

    }
}
